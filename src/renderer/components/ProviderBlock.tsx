function generateTfvars(scopes: string[]): string | null {
  if (scopes.length === 0) return null;
  const scopeList = scopes.map(s => `  "${s}",`).join('\n');
  return `# Generated by Okta Terraform Toolkit
# These are the OAuth scopes needed for your selected resources

okta_scopes = [
${scopeList}
]
`;
}

function generateImportsTf(selectedResources: ManagedResourceType[]): string | null {
  if (selectedResources.length === 0) return null;

  const lines: string[] = [
    '# Import blocks for managed resources',
    '# Replace <RESOURCE_ID> with actual Okta resource IDs',
    '# Run: terraform plan -generate-config-out=generated.tf',
    '',
  ];

  for (const type of selectedResources) {
    const mapping = TERRAFORM_RESOURCE_MAPPINGS.find(m => m.managedType === type);
    if (!mapping) continue;

    lines.push(`# ${mapping.managedType}`);
    lines.push(`# import {`);
    lines.push(`#   to = ${mapping.terraformType}.example`);
    lines.push(`#   id = "<RESOURCE_ID>"`);
    lines.push(`# }`);
    lines.push('');
  }

  return lines.join('\n');
}

