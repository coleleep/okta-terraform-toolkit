function generateResourcesTf(selectedResources: ManagedResourceType[], providerVersion: string): string | null {
  if (selectedResources.length === 0) return null;

  const sections: string[] = [
    '# Resource configurations for selected Okta resources',
    `# Generated by Okta Terraform Toolkit (v${providerVersion})`,
    '# Uncomment and customize the resources you need.',
    '',
  ];

  // Version-specific additions grouped by resource type
  const additions = getAdditionsForVersion(providerVersion);
  const additionsByType = new Map<string, string[]>();
  for (const a of additions) {
    if (!additionsByType.has(a.type)) additionsByType.set(a.type, []);
    additionsByType.get(a.type)!.push(a.config);
  }

  for (const type of selectedResources) {
    const config = RESOURCE_CONFIGS[type];
    if (config) {
      sections.push(config);
    }
    // Append version-specific resources for this type
    const typeAdditions = additionsByType.get(type);
    if (typeAdditions) {
      sections.push(...typeAdditions);
    }
  }

  return sections.join('\n');
}

type TabId = 'versions' | 'variables' | 'provider' | 'resources' | 'imports' | 'tfvars';

export default function ProviderBlock({ config, orgUrl }: Props) {
  const { selectedResources, operation, terraformAuthMethod, providerVersion, saveTfFile, saveProjectDir } = useStore();
  const [activeTab, setActiveTab] = useState<TabId>('provider');
  const [copied, setCopied] = useState(false);
  const [saved, setSaved] = useState<string | null>(null);

  const authMethod = terraformAuthMethod;
  const recs = selectedResources.length > 0
    ? getRecommendations(selectedResources, operation)
    : null;
  const oauthScopes = recs?.scopes ?? [];

  const resourcesTf = generateResourcesTf(selectedResources, providerVersion);
  const importsTf = generateImportsTf(selectedResources);
  const tfvarsContent = authMethod === 'oauth' ? generateTfvars(oauthScopes) : null;

  const files: Record<TabId, { filename: string; content: string } | null> = {
    versions: { filename: 'versions.tf', content: generateVersionsTf(providerVersion) },
    variables: { filename: 'variables.tf', content: generateVariablesTf(authMethod) },
    provider: { filename: 'provider.tf', content: generateProviderTf(config, orgUrl, authMethod, oauthScopes) },
    resources: resourcesTf ? { filename: 'resources.tf', content: resourcesTf } : null,
    imports: importsTf ? { filename: 'imports.tf', content: importsTf } : null,
    tfvars: tfvarsContent ? { filename: 'terraform.tfvars', content: tfvarsContent } : null,
  };

  const tabs: { id: TabId; label: string }[] = [
    { id: 'versions', label: 'versions.tf' },
    { id: 'variables', label: 'variables.tf' },
    { id: 'provider', label: 'provider.tf' },
    ...(files.resources ? [{ id: 'resources' as TabId, label: 'resources.tf' }] : []),
    ...(files.imports ? [{ id: 'imports' as TabId, label: 'imports.tf' }] : []),
    ...(files.tfvars ? [{ id: 'tfvars' as TabId, label: 'terraform.tfvars' }] : []),
  ];

  const currentFile = files[activeTab];

  const handleCopy = async () => {
    if (!currentFile) return;
    await navigator.clipboard.writeText(currentFile.content);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const handleSaveSingle = async () => {
    if (!currentFile) return;
    const filePath = await saveTfFile(currentFile.content);
    if (filePath) {
      setSaved(filePath);
      setTimeout(() => setSaved(null), 3000);
    }
  };

  const handleSaveProject = async () => {
    const projectFiles: Record<string, string> = {};
    for (const file of Object.values(files)) {
      if (file) projectFiles[file.filename] = file.content;
    }
    const dir = await saveProjectDir(projectFiles);
    if (dir) {
      setSaved(dir);
      setTimeout(() => setSaved(null), 4000);
    }
  };

  return (
    <div className="p-5">
      {/* File tabs */}
      <div className="flex gap-1 mb-3 border-b border-gray-200 flex-wrap">
        {tabs.map((tab) => (
          <button
            key={tab.id}
            onClick={() => setActiveTab(tab.id)}
            className={`
              px-3 py-1.5 text-xs font-mono rounded-t-lg transition-colors -mb-px border
              ${activeTab === tab.id
                ? 'bg-gray-900 text-gray-100 border-gray-900'
                : 'bg-gray-50 text-gray-500 border-gray-200 hover:bg-gray-100'}
            `}
          >
            {tab.label}
          </button>
        ))}
      </div>

      {/* Code block */}
      {currentFile && (
        <pre className="bg-gray-900 text-gray-100 rounded-lg p-4 text-sm font-mono overflow-x-auto leading-relaxed max-h-80 overflow-y-auto">
          {currentFile.content}
        </pre>
      )}

      {/* Actions */}
      <div className="flex items-center gap-3 mt-4">
        <button
          onClick={handleCopy}
          className="px-4 py-2 text-sm font-medium text-gray-700 bg-gray-100 rounded-lg hover:bg-gray-200 transition-colors"
        >
          {copied ? 'Copied!' : 'Copy File'}
        </button>
        <button
          onClick={handleSaveSingle}
          className="px-4 py-2 text-sm font-medium text-gray-700 bg-gray-100 rounded-lg hover:bg-gray-200 transition-colors"
        >
          Save File
        </button>
        <button
          onClick={handleSaveProject}
          className="px-4 py-2 text-sm font-medium text-white bg-blue-600 rounded-lg hover:bg-blue-700 transition-colors"
        >
          Export Full Project
        </button>
        {saved && (
          <span className="text-sm text-green-600">
            Saved to {saved}
          </span>
        )}
      </div>
    </div>
  );
}
